#include <iostream>
using namespace std;

int main() {
    int i = 1;
    // while loop: condition true hone tak loop chalta hai
    cout << "while loop example: Print numbers 1 to 5\n";
    while (i <= 5) {
        cout << i << " ";
        i++;  // increment
    }
    cout << endl;
    return 0;
}
