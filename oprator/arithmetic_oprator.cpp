//  Arithmetic Operators (+, -, *, /, %)


#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 3;
    cout << "a + b = " << a + b << endl;  // Addition
    cout << "a - b = " << a - b << endl;  // Subtraction
    cout << "a * b = " << a * b << endl;  // Multiplication
    cout << "a / b = " << a / b << endl;  // Division (integer)
    cout << "a % b = " << a % b << endl;  // Modulus
    return 0;
}
